//! Field metadata for SVM config form rendering.
//!
//! Provides static metadata for every field in the SVM config, including labels,
//! descriptions, input types, and subgroup organization. Used by templates to
//! render forms without hardcoding field information.

#![allow(dead_code)] // Used in later tasks

#[derive(Debug, Clone)]
pub struct SectionMeta {
    pub key: &'static str,
    pub label: &'static str,
    pub enable_field: Option<&'static str>,
}

#[derive(Debug, Clone)]
pub struct FieldMeta {
    pub path: &'static str,
    pub label: &'static str,
    pub description: &'static str,
    pub input_type: InputType,
    pub subgroup: Option<&'static str>,
}

#[derive(Debug, Clone)]
pub enum InputType {
    Toggle,
    Integer {
        min: Option<i64>,
        max: Option<i64>,
    },
    Float {
        min: Option<f64>,
        max: Option<f64>,
        step: Option<f64>,
    },
    Text,
    Textarea,
}

